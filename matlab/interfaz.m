function peso_Callback(hObject, eventdata, handles)
% hObject    handle to peso (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%   Calculo posicion
c2n=case2;
c3n=case3;
c4n=case4;
c5n=case5;
c6n=case6;
% vector medicion galgas en uE
cg=[-143.64, 95.76, -191.52, -810.84, 810.84, 810.84, -810.84]; 
pp=posicion(cg,c2n,c3n,c4n,c5n,c6n);    % calculo posicion
t=calculo_tension(cg);                  % calculo vector tension en MPa
N=calculo_axiles(t);                    % calculo vector axiles en N

if(pp==2);
    imshow('caso2.jpg');
end
if(pp==3);
    imshow('caso3.jpg');
end
if(pp==4);
    imshow('caso4.jpg');
end
if(pp==5);
    imshow('caso5.jpg');
end
if(pp==6);
    imshow('caso6.jpg');
end
axis equal;
    
if(pp==2);
    F=calculo_caso2(N);    
else 
    F=calculo_peso(N);
end

set(handles.text1,'String',F);


